let opener = document.getElementById('open');
let editor = document.getElementById('editor');
let board = document.getElementById('board');
let closer = document.getElementById('close');
opener.addEventListener('click', () => {editor.classList.add('active')});
closer.addEventListener('click', () => {editor.classList.remove('active')});
let addLineButton = document.getElementById('add-line');
addLineButton.addEventListener('click', addLine);

function addLine(event){
  let line = document.createElement('div');
  line.classList.add('line');
  let disp = document.createElement('div');
  disp.classList.add('display');
    disp.classList.add('text');
    let inp = document.createElement('input');
    inp.classList.add('text');
    let akk = document.createElement('div');
    akk.classList.add('akkord');
    let boi = document.createElement('div');
    boi.classList.add('boi');
    let vocals = document.createElement('div');
    vocals.classList.add('vocals');
    disp.append(inp);
    disp.append(akk);
    disp.append(boi);
    disp.append(vocals);
  let mode = document.createElement('div');
    mode.classList.add('mode');
    let bInp = document.createElement('button');
    bInp.classList.add('change-mode');
    bInp.classList.add('active');
    bInp.type = 'button';
    bInp.name = 'text';
    bInp.innerHTML = 'Текст';
    let bAkk = document.createElement('button');
    bAkk.classList.add('change-mode');
    bAkk.type = 'button';
    bAkk.name = 'akkord';
    bAkk.innerHTML = 'Аккорды';
    let bBoi = document.createElement('button');
    bBoi.classList.add('change-mode');
    bBoi.type = 'button';
    bBoi.name = 'boi';
    bBoi.innerHTML = 'Бой';
    let bVoc = document.createElement('button');
    bVoc.classList.add('change-mode');
    bVoc.type = 'button';
    bVoc.name = 'vocals';
    bVoc.innerHTML = 'Мотив';
    mode.append(bInp);
    mode.append(bAkk);
    mode.append(bBoi);
    mode.append(bVoc);
  line.append(disp);
  line.append(mode);
  board.append(line);
}
